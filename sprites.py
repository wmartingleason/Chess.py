def get_image(piece,piece_color,file,rank):
    if (file + str(rank)) in dark_squares:
        third_index = 1
    else:
        third_index = 0
    if piece == None:
        return (squares[third_index])
    if piece == 'pawn': # Finding the index of the sprite based on the given input.
        first_index = 0
    elif piece == 'rook':
        first_index = 1
    elif piece == 'knight':
        first_index = 2
    elif piece == 'bishop':
        first_index = 3
    elif piece == 'queen':
        first_index = 4
    else:
        first_index = 5
    if piece_color == 'white':
        second_index = 0
    else:
        second_index = 1 # The images are organized into nested lists such that each piece has its own index, each color piece does, and each square color.
    return (pieces[first_index][second_index][third_index])

